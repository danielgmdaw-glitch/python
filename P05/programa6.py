a = 10
b = 3

x = True
y = False

# Operadores aritméticos
print(a, "+", b, "=", a + b)
print(a, "-", b, "=", a - b)
print(a, "*", b, "=", a * b)
print(a, "/", b, "=", a / b)
print(a, "//", b, "=", a // b)   # división entera
print(a, "%", b, "=", a % b)     # resto
print(a, "**", b, "=", a ** b)   # potencia

print()

# Operadores lógicos
print(x, "and", y, "=", x and y)
print(x, "or", y, "=", x or y)
print("not", x, "=", not x)

print()

# Operadores de comparación
print(a, "==", b, "->", a == b)
print(a, "!=", b, "->", a != b)
print(a, ">", b, "->", a > b)
print(a, "<", b, "->", a < b)
print(a, ">=", b, "->", a >= b)
print(a, "<=", b, "->", a <= b)
print(x, "==", y, "->", x == y)
print(x, "!=", y, "->", x != y)